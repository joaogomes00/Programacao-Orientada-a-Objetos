package bo;

public class Jogadores {
	
	private String id;
	private String numCamisa;
    private String nome;
    private String time;
    private String jogosD;
    private String cartao;
    private String gols;
    
    
    
    
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNumCamisa() {
		return numCamisa;
	}
	public void setNumCamisa(String numCamisa) {
		this.numCamisa = numCamisa;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getJogosD() {
		return jogosD;
	}
	public void setJogosD(String jogosD) {
		this.jogosD = jogosD;
	}
	public String getCartao() {
		return cartao;
	}
	public void setCartao(String cartao) {
		this.cartao = cartao;
	}
	public String getGols() {
		return gols;
	}
	public void setGols(String gols) {
		this.gols = gols;
	}
    
    
    

	
	
}
