package EXEMPLOS;

public class Matematica {

    public Integer somar( Integer num1, Integer num2){

        return num1 + num2;
    }

    public Double dividir( Integer num1, Integer num2){

        return Double.valueOf(num1) / Double.valueOf(num2);
    }

    public Integer multiplicar( Integer num1, Integer num2){

        return num1 * num2;
    }

    public Integer diminuir( Integer num1, Integer num2){

        return num1 - num2;
    }





}
